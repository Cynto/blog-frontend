import type { NextPage } from 'next';
import Router, { useRouter } from 'next/router';
import { useEffect, useState } from 'react';
import Link from 'next/link';
import Head from 'next/head';
import Image from 'next/image';
import styles from '../styles/Home.module.scss';
import dynamic from 'next/dynamic';

const Header = dynamic(() => import('../components/Header'), { ssr: false });
import FeaturedPost from '../components/frontPage/FeaturedPost';
const FrontPageMain = dynamic(
  () => import('../components/frontPage/FrontPageMain'),
  { ssr: false }
);
import blogPostObjInterface from '../shared/interfaces/blogPostObj.interface';

export async function getServerSideProps() {
  const data = await fetch(
    'https://bloggy-api-cynto.herokuapp.com/posts/published'
  );

  const posts: any[] = await data.json();
  if (posts[0]) {
    const featuredIndex = posts.findIndex((post) => post.featured === true);
    if (featuredIndex !== -1) {
      posts.unshift(posts.splice(featuredIndex, 1)[0]);
    } else {
      posts[0].featured = true;
    }
  }
  return {
    props: {
      posts,
    },
  };
}

const Home: NextPage<{ posts: blogPostObjInterface[] }> = ({ posts }) => {
  return (
    <>
      <Head>
        <title>Bloggy</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Header />
      <div className="h-full relative dark:bg-gray-900 dark:text-slate-100">
        <main className="min-h-screen h-screen pt-[4.6rem]  flex flex-col justify-start">
          {posts[0] ? (
            <>
              <FeaturedPost posts={posts} />
              <FrontPageMain posts={posts} />
            </>
          ) : null}
        </main>
      </div>
    </>
  );
};

export default Home;
